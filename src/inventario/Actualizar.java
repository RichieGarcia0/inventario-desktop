    public void actualizar(int id, int eje) {
        try {
            MySQLConnection bd = new MySQLConnection();
            Connection c = bd.Conectar();
            ResultSet rs;
            PreparedStatement pst;
            String sql = "UPDATE gacetas SET ejemplaresHemeroteca = ? WHERE id = ?";
            pst = c.prepareStatement(sql);
            pst.setInt(1, eje);
            pst.setInt(2, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Se actualizó los datos correctamente", null, JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            MySQLConnection bd = new MySQLConnection();
            bd.Desconectar();
        }
    }

    public void historial(int id, int eje, String nom) {
        try {
            int hNumGac = 0;
            String hTipo = null;
            int sNumTomo = 0;
            String sFecEje = null;
            int sEje = eje; //Función
            String sNombre = nom; // Función
            MySQLConnection bd = new MySQLConnection();
            Connection c = bd.Conectar();
            PreparedStatement pst;
            ResultSet rs;
            String sql = "SELECT * FROM gacetas WHERE id = " + id;
            pst = c.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                hNumGac = rs.getInt(2);
                hTipo = rs.getString(4);
                sNumTomo = rs.getInt(7);
                sFecEje = rs.getString(5);
            }
            String sql2 = "INSERT INTO modificaciones (numeroGaceta, tipo, numeroTomo, fechaEjemplar, ejemplares, nombre,"
                    + "fechaBaja,razon,cliente,referencia) VALUES (?,?,?,?,?,?,now(),?,?,?)";
            pst = c.prepareStatement(sql2);
            pst.setInt(1, hNumGac);
            pst.setString(2, hTipo);
            pst.setInt(3, sNumTomo);
            pst.setString(4, sFecEje);
            pst.setInt(5, sEje);
            pst.setString(6, sNombre);
            pst.setString(7, cRazon.getText());
            pst.setString(8, cNomEnt.getText());
            pst.setString(9, cNumRef.getText());
            pst.execute();
            bd.Desconectar();
        } catch (SQLException ex) {
            Logger.getLogger(Alta.class.getName()).log(Level.SEVERE, null, ex);
            MySQLConnection bd = new MySQLConnection();
            bd.Desconectar();
        }
    }

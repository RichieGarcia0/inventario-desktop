    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
        // TODO add your handling code here:
        if (cRazon.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Razón está vacío", null, JOptionPane.ERROR_MESSAGE);
            return;
        }
        actualizar(sID, Integer.parseInt(cEjeHem.getText()));
        int aux = (Integer.parseInt(cEjeHem.getText())) - sEjeHem;
        historial(sID, aux, sNombre);
    }//GEN-LAST:event_btGuardarActionPerformed

    private void btMenosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMenosActionPerformed
        // TODO add your handling code here:        
        int num = Integer.parseInt(cEjeHem.getText());
        num--;
        if (num == sEjeHem) {
            jPanel2.setVisible(false);
            jPanel3.setVisible(false);
            btGuardar.setEnabled(false);
            cEjeHem.setText(Integer.toString(num));
            return;
        }
        if (num >= 0 && num < sEjeHem) {
            jPanel2.setVisible(true);
            jPanel3.setVisible(true);
            btGuardar.setEnabled(true);
            cEjeHem.setText(Integer.toString(num));
            return;
        }
        if (num > sEjeHem) {
            cEjeHem.setText(Integer.toString(num));
            return;
        }
    }//GEN-LAST:event_btMenosActionPerformed

    private void btMasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMasActionPerformed
        // TODO add your handling code here:
        int num = Integer.parseInt(cEjeHem.getText());
        num++;
        if (num == sEjeHem) {
            jPanel2.setVisible(false);
            jPanel3.setVisible(false);
            btGuardar.setEnabled(false);
            cEjeHem.setText(Integer.toString(num));
            return;
        }
        if (num > sEjeHem) {
            jPanel2.setVisible(true);
            btGuardar.setEnabled(true);
            cEjeHem.setText(Integer.toString(num));
            return;
        }
        if (num < sEjeHem) {
            cEjeHem.setText(Integer.toString(num));
            return;
        }
    }//GEN-LAST:event_btMasActionPerformed

